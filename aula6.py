numeros = [1, 5, 4, 6, 9, 8]
print(numeros)

# Adiciona o número 15 ao final da lista
numeros.append(15)
print(numeros)

# Remove o número 6 da lista (somente a primeira ocorrência)
numeros.remove(6)
print(numeros)

# Remove o elemento que está na posição de índice 1 (no caso, o segundo elemento da lista)
numeros.pop(1)
print(numeros)

# Imprime o tamanho atual da lista (quantidade de elementos)
print(len(numeros))

# Imprime a soma de todos os números da lista
print(sum(numeros))
